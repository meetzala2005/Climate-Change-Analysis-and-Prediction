from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Image
from reportlab.platypus import Table, TableStyle
from reportlab.lib import colors
from reportlab.lib.styles import getSampleStyleSheet
import matplotlib.pyplot as plt
import os

def generate_pdf(year, co2, sea, pred):
    file_path = "Climate_Report.pdf"

    # =========================
    # 📊 CREATE GRAPH IMAGE
    # =========================
    years = list(range(2020, 2031))
    temps = [20 + (i-2020)*0.3 for i in years]

    plt.figure()
    plt.plot(years, temps)
    plt.title("Temperature Trend")
    plt.xlabel("Year")
    plt.ylabel("Temperature")
    graph_path = "temp_graph.png"
    plt.savefig(graph_path)
    plt.close()

    # =========================
    # 🌍 CREATE MAP IMAGE (STATIC)
    # =========================
    # Simple placeholder map image (you can replace with real screenshot later)
    plt.figure()
    plt.scatter([72.58], [23.03])
    plt.title("Location Map")
    map_path = "map.png"
    plt.savefig(map_path)
    plt.close()

    # =========================
    # 📄 PDF DOCUMENT
    # =========================
    doc = SimpleDocTemplate(file_path)
    styles = getSampleStyleSheet()

    elements = []

    # =========================
    # 🖼 LOGO
    # =========================
    try:
        logo = Image(r"D:\PProject\assets\cloudy.png", width=60, height=60)
        elements.append(logo)
    except:
        pass

    elements.append(Spacer(1, 10))

    # =========================
    # 🎨 TITLE
    # =========================
    elements.append(Paragraph(
        "<font size=18 color='blue'><b>Climate Prediction Report</b></font>",
        styles["Title"]
    ))

    elements.append(Spacer(1, 20))

    # =========================
    # 📋 TABLE DATA
    # =========================
    table_data = [
        ["Parameter", "Value"],
        ["Year", year],
        ["CO2 Level", co2],
        ["Sea Level", sea],
        ["Predicted Temp", f"{pred:.2f} °C"]
    ]

    table = Table(table_data)
    table.setStyle(TableStyle([
        ('BACKGROUND', (0,0), (-1,0), colors.lightblue),
        ('TEXTCOLOR', (0,0), (-1,0), colors.black),
        ('GRID', (0,0), (-1,-1), 1, colors.grey),
        ('BACKGROUND', (0,1), (-1,-1), colors.whitesmoke)
    ]))

    elements.append(table)
    elements.append(Spacer(1, 20))

    # =========================
    # 📊 GRAPH
    # =========================
    elements.append(Paragraph("<b>📊 Temperature Trend</b>", styles["Heading2"]))
    elements.append(Spacer(1, 10))
    elements.append(Image(graph_path, width=400, height=200))

    elements.append(Spacer(1, 20))

    # =========================
    # 🌍 MAP
    # =========================
    elements.append(Paragraph("<b>🌍 Location Map</b>", styles["Heading2"]))
    elements.append(Spacer(1, 10))
    elements.append(Image(map_path, width=400, height=200))

    elements.append(Spacer(1, 20))

    # =========================
    # 📝 FOOTER
    # =========================
    elements.append(Paragraph(
        "<font size=10 color='grey'>Generated by Climate AI System</font>",
        styles["Normal"]
    ))

    # BUILD PDF
    doc.build(elements)

    # =========================
    # CLEAN TEMP FILES
    # =========================
    if os.path.exists(graph_path):
        os.remove(graph_path)
    if os.path.exists(map_path):
        os.remove(map_path)

    return file_path